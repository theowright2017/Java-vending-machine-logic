package Money;

public class Change {

    private int FIVE;
    private int TEN;
    private int TWENTY;
    private int FIFTY;
    private int ONEPOUND;

    public Change(int FIVE, int TEN, int TWENTY, int FIFTY, int ONEPOUND){
        this.FIVE = FIVE;
        this.TEN = TEN;
        this.TWENTY = TWENTY;
        this.FIFTY = FIFTY;
        this.ONEPOUND = ONEPOUND;
    }
}
